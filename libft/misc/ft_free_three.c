#include "libft.h"

int		ft_free_three(char ***p)
{
	int		i;

	if (!p)
		return (0);
	i = 0;
	while (p[i])
	{
		ft_bnull(p[i], ft_ptrlen(p[i]));
		ft_free_two(p[i]);
		i++;
	}
	free(p);
	p = NULL;
	return (1);
}
