#include "libft.h"

double	ft_rint(double x)
{
	double	f;
	double	n;

	n = 0.0;
	f = ft_modf(x, &n);
	if (x > 0.0)
	{
		if (f > 0.5)
			n += 1.0;
	}
	else if (x < 0.0)
	{
		if (f < -0.5)
			n -= 1.0;
	}
	return (n);
}
