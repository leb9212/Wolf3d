#include "libft.h"

int		ft_putstr_fd(char const *s, int fd)
{
	int		ret;

	if (!s)
		return (0);
	ret = 0;
	while (s[ret])
		ret += ft_putchar_fd(s[ret], fd);
	return (ret);
}
